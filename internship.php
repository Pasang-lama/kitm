    <li>ASP.NET with C#</li>
</ul>
<p>
Students are free to choose the language they are most comfortable with to maximize their learning experience.

</p>

<h3>
Software Testing
</h3>
<p>
For students focused on quality assurance, the internship involves working on various projects where they design test cases and test the developed applications. During this period, they are taught different testing strategies, including automation and API testing, to build their expertise in software quality assurance.

</p>
<h3>
Networking and System Administration

</h3>
<p>
Students interested in networking and system administration are given hands-on experience with Cisco routers and switches. KITM also offers the Cisco Certified Network Associate (CCNA) course, providing an additional certification opportunity. Additionally, students are connected with various ISPs to begin their internship journey in networking. For those inclined towards system administration, training includes operating knowledge of Windows and Linux servers.

</p>
<h3>
UI, Graphics & Multimedia

</h3>
<p>
Students with a passion for User Interface (UI) design work with tools like Figma to create web interfaces, focusing on delivering a seamless user experience. The training covers all the necessary steps to craft an excellent user experience. Graphic Design students, on the other hand, use tools such as Photoshop, Illustrator, and InDesign to create logos, brochures, banners, magazines, and other visual content.

</p>
